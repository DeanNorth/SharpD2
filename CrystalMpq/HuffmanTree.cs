		sealed class HuffmanItem
		{
			int value;
			int weight;
			HuffmanItem next;
			HuffmanItem previous;
			HuffmanItem parent;
			HuffmanItem child;

			public int Value { get { return value; } set { this.value = value; } }
			public int Weight { get { return weight; } set { weight = value; } }

			public HuffmanItem Next { get { return next; } set { next = value; } }
			public HuffmanItem Previous { get { return previous; } set { previous = value; } }
			public HuffmanItem Parent { get { return parent; } set { parent = value; } }
			public HuffmanItem Child0 { get { return child; } set { child = value; } }
			public HuffmanItem Child1 { get { return child.previous; } }
			public HuffmanItem this[int index] { get { return (index != 0) ? child.previous : child; } }

			public void Reset(int value, int weight)
			{
				this.value = value;
				this.weight = weight;
				next = null;
				previous = null;
				parent = null;
				child = null;
			}
		}

		struct LinkedList
		{
			HuffmanItem head;
			HuffmanItem tail;

			public HuffmanItem Head { get { return head; } }
			public HuffmanItem Tail { get { return tail; } }

			public void Clear()
			{
				head = null;
				tail = null;
			}

			public void Remove(HuffmanItem item)
			{
				if (item == tail)
					tail = item.Previous;
				if (item == head)
					head = item.Next;
				if (item.Previous != null)
					item.Previous.Next = item.Next;
				if (item.Next != null)
					item.Next.Previous = item.Previous;
				item.Next = item.Previous = null;
			}

			public void InsertWeighted(HuffmanItem item)
			{
				HuffmanItem current;

				// If tail is null then head is null too
				if (tail == null)
				{
					tail = head = item;
					return;
				}

				current = tail;

				while (current != null && current.Weight < item.Weight)
					current = current.Previous;

				if (current != null)
				{
					item.Previous = current;
					item.Next = current.Next;
					current.Next.Previous = item;
					current.Next = item;

					if (item.Next == null)
						tail = item;
				}
				else
				{
					item.Previous = null;
					item.Next = head;
					current.Previous = item;
				}
			}

			public void InsertAfter(HuffmanItem item, HuffmanItem newItem)
			{
				newItem.Next = item.Next;
				newItem.Previous = item;
				if (item.Next != null)
					item.Next.Previous = newItem;
				else if (item == tail)
					tail = newItem;
				item.Next = newItem;
			}

			public void InsertBefore(HuffmanItem item, HuffmanItem newItem)
			{
				newItem.Next = item;
				newItem.Previous = item.Previous;
				if (item.Previous != null)
					item.Previous.Next = newItem;
				else if (item == head)
					head = newItem;
				item.Previous = newItem;
			}
		}

