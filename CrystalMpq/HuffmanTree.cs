		HuffmanItem[] itemArray = new HuffmanItem[0x203];
		int itemCount, usedItemCount;
		LinkedList itemList;
		HuffmanItem root;

		void ClearCache()
		{
			for (int i = 0; i < itemArray.Length; i++)
				itemArray[i] = null;
		}

		void Reset()
		{
			root = null;
			itemList.Clear();
			usedItemCount = 0;
		}

		HuffmanItem GetNewItem(int value, int weight)
		{
			HuffmanItem item;

			if (usedItemCount < itemCount)
				item = itemArray[usedItemCount++];
			else if (itemCount < itemArray.Length)
			{
				item = itemArray[itemCount++] = new HuffmanItem();
				usedItemCount++;
			}
			else
				throw new Exception("No more items");

			item.Reset(value, weight);

			return item;
		}

		void BuildList(byte[] data)
		{
			itemList.InsertWeighted(GetNewItem(256, 1));
			itemList.InsertWeighted(GetNewItem(257, 1));

			for (int i = 0; i < data.Length; i++)
				if (data[i] != 0)
					itemList.InsertWeighted(GetNewItem(i, data[i]));
		}

		void BuildTree(byte[] data)
		{
			BuildList(data);
		}

		public static int CompressBlock(byte[] inBuffer, int index, int count, byte[] outBuffer)
		{
			return 0;
		}

		public void DecompressBlock(byte[] inBuffer, int index, int count, byte[] outBuffer)
		{
			BitBuffer buffer;
			byte compressionType;

			compressionType = inBuffer[index++];
			buffer = new BitBuffer(inBuffer, index, count - 1);

			BuildTree(HuffmanTree.data[compressionType]);
		}
